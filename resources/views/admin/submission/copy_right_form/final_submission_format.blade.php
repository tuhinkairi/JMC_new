            <h3>IV. Discussion <span class="text-red">(11 Bold)</span></h3>
            <p>
                Dyslipidemia in patients with diabetes plays an important role in development of atherogenesis. The standarded of treatment for dyslipidemia have been statins.For the treatment of dyslipidemia the most commonly used statins are are atorvastatin and rosuvastatin. <span class="text-red">(10)</span></p>
            <p>
                The four major statin beneficiary groups have already been defined  by NCEP 2013 report.
                <br>
                There is a wealth of evidence suggesting that lowering low density lipoprotein cholesterol (LDL-C) reduces the risk of cardiovascular disease (CVD). Both European and US guidelines for CVD prevention recommend the use statins as first-line therapy for dyslipidemia and specify target LDL-C levels.  Previously, a National Cholesterol Education Program (NCEP) report had proposed to lower target levels to even more aggressive LDL-C goals for very high-risk patients.<br>
                Despite the proven benefits of LDL-C reduction ,lipid managenent  is suboptimal and many patients fail to achieve recommended LDL-C goals11,12..Themost likely reasons for this are the use of agents with poor efficacy for LDL-C lowering and suboptimal dose titration.<br>
                Such aggressive LDL-C goals, however are  harder to achieve. The most effective statin at the lowest dose would represent a simple, effective treatment strategy, enabling more patients to achieve goals without the need for dose titration.<br>
                Rosuvastatin, at a dose of 20 mg, has demonstrated high efficacy for LDL-C lowering, enabling patients with hypercholesterolemia to achieve their lipid goals<sup>10,11</sup>.
            </p>
            <h3>V.  Conclusion <span class="text-red">(11 Bold)</span></h3>
