            <h3>I.  Introduction <span class="text-red">(11 Bold)</span></h3>
            <p>
                Diabetes is now commonly recognized as a coronary heart disease risk  equivalent1,2,3,4.This is mainly attributed to the high rates of dyslipidemia among diabetic patients which is believed to be one of the major factors accounting for the high percentage of deaths among diabetics due to cardiovascular disease (CVD)5.Numerous epidemiological studies and randomized controlled trials have documented the association between elevated LDL-C levels with increased CVD risk in both diabetic and non diabetic populations.6,7. Thus reducing LDL-C levels is the primary goal of therapy for diabetic dyslipidemia.5,8.Statins are considered the first pharmacological line of treatment of dyslipidemia in diabetic patients9. Lowering of LDL-C levels is thought to be the main beneficial effect of statin treatment.In India currently no guidelines available for treating diabetic dyslipidemia and no previous study has documented the efficacy.The current study aims to build growing awareness of atherosclerosis specific care of diabetes patient by examining efficacy of two most commonly prescribed statins in India. <span class="text-red">(10)</span>
            </p>
            <h3>II. Material And Methods <span class="text-red">(11 Bold)</span></h3>
            <p>This prospective comparative study was carried out on patients of Department of general Medicine at Dr. Ram Manohar Lohia Combined Hospital, Vibhuti Khand, Gomti Nagar, Lucknow, Uttar Pradesh from November 2014 to November 2015. A total 300 adult subjects (both male and females) of aged ≥ 18, years were for in this study.<span class="text-red">(10)</span><br>
            <strong>Study Design:</strong> Prospective open label observational study<br>
            <strong>Study Location:</strong> This was a tertiary care teaching hospital based study done in Department of General Medicine, at Dr. Ram Manohar Lohia Combined Hospital, Vibhuti Khand, Gomti Nagar, Lucknow, Uttar Pradesh.<span class="text-red">(10)</span></p>
            <p class="mb-0">
                <strong>Study Duration:</strong> November 2014 to November 2015.<br>
                <strong>Sample size:</strong> 300 patients.<br>
                <strong>Sample size calculation:</strong> The sample size was estimated on the basis of a single proportion design. The target population from which we randomly selected our sample was considered 20,000. We assumed that the confidence interval of 10% and confidencelevel of 95%. The sample size actually obtained for this study was 96 patients for each group. We planned to include 300 patients (Group I- Control, Group II- Cases of 100 patients for each group) with 4% drop out rate.<span class="text-red">(10)</span><br>
                <strong>Subjects & selection method:</strong> The study population was drawn from consecutive diabetic patients who presented to Dr. Ram ManoharLohia Combined Hospital with dyslipidemia and were prescribed the indicated statins and underwent fasting blood test of lipid profile before statin treatment initiation between from November 2014 to November 2015. Patients were divided into three groups (each group had 100 patients) according to doses of statins. The prescribed doses of statin in RMLH for diabetic patients <span class="text-red">(10)</span><br>
                With dyslipidemia were as follows:
            </p>
            <ul>
                <li>Group A(N=100 patients) -Atorvaststin 40mg daily to each patients;</li>
                <li>Group B (N=100 patients) -Rosuvastatin 20mg daily to each patients; and</li>
                <li>Group C (N=100 patients) -Rosuvastatin 20 mg to each patients at alternative days.</li>
            </ul>
            <strong>Inclusion criteria: <span class="text-red">(10) Bold</span></strong>
            <ul>
                <li>1.  Diabetic patients (fasting blood glucose ≥ 126 mg/dL [7.0mmol/L])</li>
                <li>2.  Either sex</li>
                <li>3.  Aged ≥ 18 years,</li>
                <li>4.  Patients have a total cholesterol level of ≥154.68 mg/dl , LDL-C 96.6 mg/dl, HDL-C ≤ 138.6 in men and ≤46.3 mg/dl in women.</li>
                <li>5.  Fasting triglycerides ≥ 150.56 mg/dl, obtained within 1 week before the first use of statins which was then compared at first- and second-year intervals.<span class="text-red">(10)</span></li>
            </ul>
            
            <strong>Exclusion criteria: <span class="text-red">(10) Bold</span></strong>
            <ul>
                <li>1.  Pregnant women;</li>
                <li>2.  Patients with genetic disorders</li>
                <li>3.  Patients on other concurrent lipid lowering agents such as bile acid sequest</li>
                <li>4.  rants (cholestyramine, colesevelam), niacin, ezetimibe, fenofibrate and/or omega 3.</li>
                <li>5.  Patients with previous history of angina, severe vascular disease, or other life threatening disease.</li>
                <li>6.  Patients with nephropathy and/or hypothyroidism, active liver disease, bile duct problems, or ALT > 3 × ULN.</li>
                <li>7.  Patients with creatine kinase levels > 10 × ULN.</li>
                <li>8.  Patients taking concurrent corticosteroids, ciclosporin, and/or hormone replacement therapy.</li>
                <li>9.  Patients who are physically inactive.</li>
                <li>10. Patients with a history of drug or alcohol abuse. </li>
            </ul>
        

            <strong>Statistical analysis <span class="text-red">(10) Bold</span></strong>
            <p>Data was analyzed using SPSS version 20 (SPSS Inc., Chicago, IL). Student's t-test was used to ascertain the significance of differences between mean values of two continuous variables and confirmed by nonparametric Mann-Whitney test. In addition, paired t-test was used to determine the difference between baseline and 2 years after regarding biochemistry parameters, and this was confirmed by the Wilcoxon test which wasa nonparametric test that compares two paired groups. Chi-square and Fisher exact tests were performed to test for differences in proportions of categorical variables between two or more groups. The level P < 0.05 was considered as the cutoff value or significance. <span class="text-red">(10)</span></p>
